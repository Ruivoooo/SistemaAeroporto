    public void listarVoos(){
        
        VooController vooController = new VooController();
        List<Voo> lista = vooController.read();
        DefaultTableModel dados = (DefaultTableModel) ConsultarVoo.getModel();
        dados.setNumRows(0);
        
        for(Voo voo : lista){
        
            dados.addRow(new Object[]{
                voo.getOrigem(),
                voo.getDestino(),
                voo.getStatus(),
        });
    }
}
    
