    public void listar(){
        
        BagagemController bagagemController = new BagagemController();
        List<Bagagem> lista = bagagemController.read();
        DefaultTableModel dados = (DefaultTableModel)ConsultarBagagem.getModel();
        dados.setNumRows(0);
        
        for(Bagagem bagagem : lista ){
        dados.addRow(new Object[]{
            bagagem.getIdBagagem(),
            bagagem.getPesoKg(),
            bagagem.getDestinoFinal(),
            bagagem.getStatus()
        });
        }
    }
    
   private void listarCheckIn(){
        CheckInController checkInController = new CheckInController();
        List<CheckIn> listarCheckIn = checkInController.read();
        
        CheckInCombo.removeAllItems();
        
        CheckInCombo.addItem("Selecione...");
       
            for(CheckIn checkIn : listarCheckIn){
                
            CheckInCombo.addItem(checkIn.getIdCheckIn() + " - ");
        }
            
            CheckInCombo.setSelectedIndex(0);

    }
   
    
