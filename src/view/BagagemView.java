    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
     
         Bagagem bagagem = new Bagagem();
        

        bagagem.setPesoKg(Double.parseDouble(peso.getText()));
        bagagem.setDestinoFinal(destino.getText());
        bagagem.setStatus(status.getText());
        
        CheckIn checkIn  = new CheckIn();
        checkIn = (CheckIn) CheckInCombo.getSelectedItem();
        bagagem.setCheckIn(checkIn);
        

        bagagem.setIdBagagem(Integer.parseInt(id.getText()));
        
        BagagemController bagagemController = new BagagemController();
        bagagemController.delete(bagagem);

        JOptionPane.showMessageDialog(null,"Bagagem excluida com sucesso!");
        
       limpar();
       Excluir.setEnabled(false); 

    }//GEN-LAST:event_ExcluirActionPerformed

    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed

        
        try{

            if (peso.getText().trim().isEmpty() ||
                    destino.getText().trim().isEmpty() ||
                    status.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!");
                return;
            }

            int capacidade = Integer.parseInt(peso.getText().trim());
            if (capacidade <= 0) {
                JOptionPane.showMessageDialog(null, "O peso deve ser um número maior que zero!");
                return;
            }
        
        if(id.getText().equals("0")){
        Bagagem bagagem = new Bagagem();
        
        bagagem.setPesoKg(Double.parseDouble(peso.getText()));
        bagagem.setDestinoFinal(destino.getText());
        bagagem.setStatus(status.getText());
        
        
        //
        
        String selecionado = (String) CheckInCombo.getSelectedItem();
       int idCheckIn = Integer.parseInt(selecionado.split(" - ")[0]);
       
       CheckInController checkInController = new CheckInController();
       CheckIn checkIn = checkInController.findById(idCheckIn);
       
       bagagem.setCheckIn((checkIn));
       
       //
       
       
        BagagemController bagagemController = new BagagemController();
        bagagemController.create(bagagem);
        
        int idBagagemInt = bagagemController.getLastId();
        String idBagagemString = Integer.toString(idBagagemInt);
        id.setText(idBagagemString);
        
        listar();
        limpar();
        Excluir.setEnabled(false); 
                
        JOptionPane.showMessageDialog(null,"Bagagem salvo com sucesso!");
        
        }else{
            
        Bagagem bagagem = new Bagagem();
        
        bagagem.setPesoKg(Double.parseDouble(peso.getText()));
        bagagem.setDestinoFinal(destino.getText());
        bagagem.setStatus(status.getText());
        
        
       //
        
        String selecionado = (String) CheckInCombo.getSelectedItem();
       int idCheckIn = Integer.parseInt(selecionado.split(" - ")[0]);
       
       CheckInController checkInController = new CheckInController();
       CheckIn checkIn = checkInController.findById(idCheckIn);
       
       bagagem.setCheckIn((checkIn));
       
       //
       
       
        BagagemController bagagemController = new BagagemController();
        bagagemController.update(bagagem);
        
        listar();
        limpar();
        Excluir.setEnabled(false); 
        
        JOptionPane.showMessageDialog(null,"Bagagem atualizado com sucesso!");
        
        }
        }catch(NumberFormatException e){
            
            JOptionPane.showMessageDialog(null,"O peso deve ser um número válido.");
        }
        
        
    }//GEN-LAST:event_SalvarActionPerformed

    
    
